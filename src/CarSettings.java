public class CarSettings {

    private float seatHeight;
    private float seatDistance;
    private float backPosition;
    private float sideMirrorPosition;
    private float rearviewMirrorPosition;

    public float getSeatHeight() {
        return seatHeight;
    }

    public void setSeatHeight(float seatHeight) {
        this.seatHeight = seatHeight;
    }

    public float getSeatDistance() {
        return seatDistance;
    }

    public void setSeatDistance(float seatDistance) {
        this.seatDistance = seatDistance;
    }

    public float getBackPosition() {
        return backPosition;
    }

    public void setBackPosition(float backPosition) {
        this.backPosition = backPosition;
    }

    public float getSideMirrorPosition() {
        return sideMirrorPosition;
    }

    public void setSideMirrorPosition(float sideMirrorPosition) {
        this.sideMirrorPosition = sideMirrorPosition;
    }

    public float getRearviewMirrorPosition() {
        return rearviewMirrorPosition;
    }

    public void setRearviewMirrorPosition(float rearviewMirrorPosition) {
        this.rearviewMirrorPosition = rearviewMirrorPosition;
    }
}
